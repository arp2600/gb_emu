pub fn io_reg_description(index: usize) -> String {
    match index {
        SCX => "SCX - Scroll X".to_string(),
        SC => "SC - Serial Transfer Control".to_string(),
        NR52 => "NR52 - Sound on/off".to_string(),
        NR51 => "NR51 - Selection of Sound output terminal".to_string(),
        NR50 => "NR50 - Channel control / ON-OFF / Volume".to_string(),
        NR12 => "NR12 - Channel 1 Volume Envelope".to_string(),
        NR22 => "NR22 - Channel 2 Volume Envelope".to_string(),
        NR42 => "NR42 - Channel 4 Volume Envelope".to_string(),
        NR14 => "NR14 - Channel 1 Frequency hi".to_string(),
        NR24 => "NR24 - Channel 2 Frequency hi data".to_string(),
        NR44 => "NR44 - Channel 4 Counter/consecutive; Inital".to_string(),
        NR10 => "NR10 - Channel 1 Sweep register".to_string(),
        NR30 => "NR30 - Channel 3 Sound on/off".to_string(),
        WY => "WY - Window Y Position".to_string(),
        WX => "WX - Window X Position minus 7".to_string(),
        NR11 => "NR11 - Channel 1 Sound length/Wave pattern duty".to_string(),
        NR13 => "NR13 - Channel 1 Frequency lo".to_string(),
        NR21 => "NR21 - Channel 2 Sound Length/Wave Pattern Duty".to_string(),
        NR23 => "NR23 - Channel 2 Frequency lo data".to_string(),
        WAVE_PATTERN_RAM_START...WAVE_PATTERN_RAM_END => "Wave Pattern RAM".to_string(),
        NR31 => "NR31 - Channel 3 Sound Length".to_string(),
        NR32 => "NR32 - Channel 3 Select output level".to_string(),
        NR33 => "NR33 - Channel 3 Frequency's lower data".to_string(),
        NR34 => "NR34 - Channel 3 Frequency's higher data".to_string(),
        NR41 => "NR41 - Channel 4 Sound Length".to_string(),
        NR43 => "NR43 - Channel 4 Polynomial Counter".to_string(),
        _ => format!("{:#06x}", index),
    }
}
